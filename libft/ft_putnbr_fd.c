#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	s[12];
	int		i;

	while (n == 0 || n <= -2147483647)
	{
		if (n == 0)
			write(fd, "0", 1);
		if (n == -2147483648)
			write(fd, "-2147483648", 11);
		return ;
	}
	i = 0;
	while (n)
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n *= -1;
		}
		s[i++] = (n % 10) + '0';
		n = n / 10;
	}
	i--;
	while (i >= 0 && s[i] <= '9' && s[i] >= '0')
		write(fd, &s[i--], 1);
}
